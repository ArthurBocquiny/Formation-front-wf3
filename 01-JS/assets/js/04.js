// -- l'addition

    // déclaration de plusieurs variables à la suite
    var nb1, nb2, resultat;

    nb1 = 10;
    nb2 = 5;

    // -- Addition de nb1 + nb2 avec l'opérateur " + ";
    resultat = nb1 + nb2;
    console.log( "L'addition de nb1 + nb2 est égale à  : " + resultat );
    
// -- la soustraction

    // -- soustraction de nb1 + nb2 avec l'opérateur " - ";
    resultat = nb1 - nb2;
    console.log( "La soustraction de nb1 - nb2 est égale à  : " + resultat );
    
// -- la multiplication

    // -- soustraction de nb1 + nb2 avec l'opérateur " * ";
    resultat = nb1 * nb2;
    console.log( "La multiplication de nb1 * nb2 est égale à  : " + resultat );
    
// -- la division

    // -- division de nb1 + nb2 avec l'opérateur " / ";
    resultat = nb1 / nb2;
    console.log( "La division de nb1 / nb2 est égale à  : " + resultat );
    
// -- le modulo

    nb1 = 11;

    // -- modulo de nb1 + nb2 avec l'opérateur " % ";
    resultat = nb1 % nb2;
    console.log( "Le modulo de nb1 % nb2 est égale à  : " + resultat );


