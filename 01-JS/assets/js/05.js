/* Incrementation et decrementation */

// -- incrementation

var nb1 = 1;
nb1 = nb1 + 1;

console.log(nb1);

// -- Ecriture simplifiée

nb1++; // -- affiche puis incrémente + 1
console.log(nb1);

++nb1; // -- incrémente + 1 puis affiche
console.log(nb1);

// -- decrementation

nb1 = nb1 - 1;
console.log(nb1);

nb1--; // -1
console.log(nb1);
